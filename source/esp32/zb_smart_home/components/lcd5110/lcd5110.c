void lcd5110_display_BK_logo(bool inverted) {
    lcd5110_clear();
    for (uint16_t i = 0; i < sizeof(HCMUT_official_logo); i++) {
        for (uint8_t j = 0; j < 8; j++)
            lcd5110_draw_pixel(
                i % LCD5110_WIDTH, j + (i / LCD5110_WIDTH) * 8,
                (((HCMUT_official_logo[i] & (1 << j)) != 0) ^ inverted)
                    ? LCD5110_PIXEL_SET
                    : LCD5110_PIXEL_CLEAR);
    }
    lcd5110_refresh();
}
